        public READ_W addr = READ_W.MAX;
        public READ_TYPE type = READ_TYPE.NONE;

        public int length;

        public int value;
        public string text;

        public WORD_DATA(READ_W addr)
        {
            this.addr = addr;

            string text = addr.ToString();
            string[] split = text.Split('_');

            if (split[0] == "ASCII")
                type = READ_TYPE.ASCII;
            if (split[0] == "DEC")
                type = READ_TYPE.DEC;

            length = Util.toInt32(split[1]);
        }
    }

    static string m_path = "";
    CLogManager m_cimLog = null;

    bool[] m_bit = new bool[16 * 32];
    bool[] m_agoBit = new bool[16 * 32];

    WORD_DATA[] m_wordData = new WORD_DATA[(int)READ_W.MAX];

    public static CIMRead Instance = new CIMRead();

    public CIMRead()
    {
        m_path = pathUtil.savePath();

        m_cimLog = new CLogManager("CIM_R", m_path, "", "CIM_R");

        for (int i = 0; i < m_agoBit.Count(); i++)
        {
            m_agoBit[i] = false;
        }

        for (int i = 0; i < (int)READ_W.MAX; i++)
        {
            m_wordData[i] = new WORD_DATA((READ_W)i);
        }
    }

    public bool[] data()
    {
        return m_bit;
    }

    public void toBitArray(int[] arr)
    {
        int intCount = arr.Length;

        for (int i = 0; i < intCount; i++)
            Util.wordToBit(arr[i], ref m_bit, i * 32);

        for (int i = 0; i < m_bit.Count(); i++)
        {
            if (m_bit[i] != m_agoBit[i])
            {
                string timeString = "[" + DateTime.Now.ToString("HHmmss.ffff") + "]";

                string startAddr = "0x00";
                
                m_cimLog.write(timeString + " CIM CHANGED::address:" + startAddr + i.ToString("X2") + " desc:" + (READ_B)i + " value:" + m_bit[i].ToString());
                m_agoBit[i] = m_bit[i];
            }
        }
    }

    public void toWordArray(int[] arr)
    {
        int readAddress = 0;

        for (int i = 0; i < m_wordData.Length; i++)
        {
            WORD_DATA data = m_wordData[i];

            int length = data.length;
            int[] value = new int[length];

            for (int j = 0; j < length; j++)
            {
                if (readAddress > arr.Length - 1)
                    return;

                value[j] = arr[readAddress];
                readAddress++;
            }

            if (data.type == READ_TYPE.DEC)
            {
                if (length == 2)
                {
                    data.value = ((int)(ushort)value[1] << 16) | (ushort)value[0];
                }
                else
                {
                    data.value = (ushort)value[0];
                }
            }

            if (data.type == READ_TYPE.ASCII)
            {
                data.text = Util.toString(value, 0, value.Length);
            }
        }
    }

    public bool readBit(READ_B addr)
    {
        return readBit((int)addr);
    }

    public bool readBit(int addr)
    {
        return m_bit[addr];
    }

    public WORD_DATA wordData(READ_W addr)
    {
        return m_wordData[(int)addr];
    }
}
