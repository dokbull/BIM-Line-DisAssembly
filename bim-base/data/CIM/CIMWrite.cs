    public class WORD_DATA
    {
        public WRITE_W addr = WRITE_W.MAX;
        public WRITE_TYPE type = WRITE_TYPE.NONE;

        public int length;

        public int value;
        public string text;

        public WORD_DATA(WRITE_W addr)
        {
            this.addr = addr;

            string text = addr.ToString();
            string[] split = text.Split('_');

            if (split[0] == "ASCII")
                type = WRITE_TYPE.ASCII;
            if (split[0] == "DEC")
                type = WRITE_TYPE.DEC;

            length = Util.toInt32(split[1]);
        }
    }


    static string m_path = "";
    CLogManager m_cimLog = null;

    //bool[] m_bit = new bool[32 * 16];
    bool[] m_bit = new bool[32 * 16 * 2];
    bool[] m_agoBit = new bool[32 * 16 * 2];

    WORD_DATA[] m_wordData = new WORD_DATA[(int)WRITE_W.MAX];

    DateTime m_cimDateTime = new DateTime();


    public CIMWrite()
    {
        m_path = pathUtil.savePath();

        m_cimLog = new CLogManager("CIM_W", m_path, "", "CIM_W");

        m_cimDateTime = DateTime.Now;

        for (int i = 0; i < m_agoBit.Count(); i++)
        {
            m_agoBit[i] = false;
        }

        for (int i = 0; i < (int)WRITE_W.MAX; i++)
        {
            m_wordData[i] = new WORD_DATA((WRITE_W)i);
        }
    }

    public void setBit(int index, bool value)
    {
        m_bit[index] = value;
    }

    public void setBit(WRITE_B addr, bool value)
    {
        if (addr == WRITE_B.PPIDCHANGE_21)
        {
            int aa = 900;
        }

        setBit((int)addr, value);
    }

    public bool bit(WRITE_B addr)
    {
        return m_bit[(int)addr];
    }

    public void toArrayB(ref int[] arr)
    {
        int cnt = arr.Length;

        for (int i = 0; i < cnt; i++)
            arr[i] = Util.bitToWord(m_bit, i * 32);

#if true // CIM 기록 코드
        for (int i = 0; i < m_bit.Count(); i++)
        {
            if (m_bit[i] != m_agoBit[i])
            {
                string timeString = "[" + DateTime.Now.ToString("HHmmss.ffff") + "]";

                string addr = "0x00";

                m_cimLog.write(timeString + " PC CHANGED::address:" + addr + i.ToString("X2") + " desc:" + (WRITE_B)i + " value:" + m_bit[i].ToString());
                m_agoBit[i] = m_bit[i];
            }
        }
#endif
    }

    public void toArrayW(ref int[] arr)
    {
        int writeAddress = 0;

        for (int i = 0; i < m_wordData.Length; i++)
        {
            WORD_DATA data = m_wordData[i];

            int length = data.length;
            int[] value = new int[length];

            if (data.type == WRITE_TYPE.DEC)
            {
                if (length == 2)
                {
                    value[0] = data.value & 0xFFFF;
                    value[1] = (data.value >> 16) & 0xFFFF; 
                }
                else
                {
                    value[0] = data.value & 0xFFFF;
                }
            }

            if (data.type == WRITE_TYPE.ASCII || data.type == WRITE_TYPE.NONE)
            {
                string text = data.text;

                if (text == null)
                {
                    writeAddress += data.length;
                    continue;
                }

                byte[] bytes = Encoding.ASCII.GetBytes(text);

                for (int j = 0; j < length; j++)
                {
                    byte b0 = 0x20;
                    byte b1 = 0x20;

                    int byteIndex = j * 2;

                    if (byteIndex < bytes.Length)
                        b0 = bytes[byteIndex];

                    if (byteIndex + 1 < bytes.Length)
                        b1 = bytes[byteIndex + 1];

                    value[j] = b0 | (b1 << 8);
                }
            }

            for (int j = 0; j < length; j++)
            {
                if (writeAddress > arr.Length - 1)
                    return;

                arr[writeAddress] = value[j];
                writeAddress++;
            }
        }
    }

    public WORD_DATA wordData(WRITE_W addr)
    {
        return m_wordData[(int)addr];
    }
